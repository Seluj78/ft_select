#include "../includes/21sh.h"

char **lexer(char *str)
{
	char **ret;
	int	*placement;
	int i;
	int j;

	j = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == ' ' || str[i] == '\t'  || str[i] == ';' || str[i] == '|' \
				|| str[i] == '<' || str[i] == '>' || \
				(str[i] == '&' && str[i + 1] == '&') || \
				(str[i] == '|' && str[i + 1] == '|') || \
				(str[i] == '<' && str[i + 1] == '<') || \
				(str[i] == '>' && str[i + 1] == '>'))
			j++;
		i++;
	}
	i = 0;
	placement = (int *)malloc(sizeof(int) * j);
	ret = ft_strsplit(str, ' ');
	return (ret);
}

